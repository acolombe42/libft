#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char	c1;

	c1 = (unsigned char)c;
		if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}
