#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	j;

	j = 0;
	if (s != NULL)
	{
		i = ft_strlen(s);
		while (j != i)
		{
			s[j] = '\0';
			j++;
		}
	}
}
