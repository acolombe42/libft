#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	size_t			i;
	unsigned char	*s2;

	s2 = (unsigned char*)s;
	i = 0;
	while (i != len && len != 0)
	{
		s2[i] = 0;
		i++;
	}
}
